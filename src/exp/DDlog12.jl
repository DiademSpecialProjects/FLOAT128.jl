# for x in 0.5..1..2
function log12(x::DD)
    isneg, ax = signbit(x.hi), abs(x)
    if ax == dd_one
       fx = dd_zero
    elseif isneg
       fx = dd_NaN
    else
       if x.hi < one(typeof(x.hi))
          ax = 1.0/ax
          fx = -log12approx[ 1+(floor(Int, (ax.hi-64) * 64)) ](ax)
       else
          fx =  log12approx[ 1+(floor(Int, (ax.hi-64) * 64)) ](ax)
       end
    end
    fx
end    
